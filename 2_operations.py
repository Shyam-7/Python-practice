#operations.py using type hints

# arithmetic operations
addition: int = 5+3
subtraction:int = 10-2
multiplication: int = 4*2
division: float = 10/2
modulus: int = 10 % 3
exponentiation: float = 2 ** 3
floor_division: int = 10 // 3

# bitwise operations
bitwise_and: int = 5 & 3
bitwise_or: int = 5 | 3
bitwise_xor: int = 5 ^ 3
bitwise_not: int = ~5
left_shift: int = 5 << 2
right_shift: int = 5 >> 2

# logical operations
logical_and: bool = True and False
logical_or: bool = True or False
logical_not: bool = not True

